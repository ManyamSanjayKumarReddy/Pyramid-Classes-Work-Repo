package Day_5.Strings;

public class Creation_of_Strung {
    public static void main(String[] args){
        String sanju = "Sanju ";
        String basha = "Basha ";
        String vinay = "Vinay";

        // Concatenation of Strings
        String join = sanju.concat(basha);
        System.out.println(join);

        System.out.println(sanju + basha + vinay);

    }
}
